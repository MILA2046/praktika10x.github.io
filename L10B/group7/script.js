function toggleText() {
    var text = document.getElementById("hiddenText");
    if (text.style.display === "none") {
        text.style.display = "block";
    } else {
        text.style.display = "none";
    }
}
function  ttoggleText() {
    var text = document.getElementById("hhiddenText");
    if (text.style.display === "none") {
        text.style.display = "block";
    } else {
        text.style.display = "none";
    }
}
